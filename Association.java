public class Association {
    private String ingles;
    private String espannol;
    private String frances;

    public Association(String ingles, String espannol, String frances) {
        this.ingles = ingles;
        this.espannol = espannol;
        this.frances = frances;
    }

    public String getIngles() {
        return ingles;
    }

    public String getEspannol() {
        return espannol;
    }

    public String getFrancia() {
        return frances;
    }
}